package variable;
/*
    변수 타입
    char -> 문자 하나를 다룸 ,'' (작은 따옴표 사용)
    String -> 문자열을 다룬다. "" (큰 따옴표 사용)
 */
public class Var7 {
    public static void main(String[] args) {
        int a = 100; //정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언 -> true, false
        char d = 'A'; //문자 하나
        String e = "Hello JinSu"; // 문자열, 문자열을 다루기 위한 특별한 타입
        // 위에서 100, 10.5, true, A, Hello JinSu를 리터럴이라고 한다
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e); // ctrl + d : 복붙

    }
}
