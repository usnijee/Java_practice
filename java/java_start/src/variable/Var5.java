package variable;
/*
    변수 초기화
 */

public class Var5 {
    public static void main(String[] args) {
        int a;
        a = 1;
        System.out.println(a);

        int b = 2; // 변수의 선언과 초기화를 한번에
        System.out.println(b);

        int c = 3, d = 4;  // 여러 변수의 선언과 초기화를 한번에
        System.out.println(c);
        System.out.println(d);
    }

}
